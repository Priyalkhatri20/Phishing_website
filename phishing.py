    def _reconstruct_csv(self, file_path: str) -> pd.DataFrame:
        """Manually reconstruct CSV file"""
        print("üî® Reconstructing CSV manually...")
        rows = []
        headers = None
        skipped_rows = 0

        with open(file_path, 'r', encoding='utf-8', errors='ignore') as file:
            # Get headers
            first_line = file.readline().strip()
            headers = [col.strip() for col in first_line.split(',')]
            print(f"üìã Headers detected: {headers}")

            # Read data
            for line_num, line in enumerate(file, 2):
                try:
                    # Simple comma split (ignore quotes)
                    row = [col.strip().strip('"') for col in line.strip().split(',')]

                    # Ensure correct number of columns
                    if len(row) >= len(headers):
                        if len(row) > len(headers):
                            # Join extra columns to last column
                            extra = ' '.join(row[len(headers)-1:])
                            row = row[:len(headers)-1] + [extra]
                        rows.append(row[:len(headers)])
                    elif len(row) == len(headers) - 1:
                        # Add empty last column if needed
                        row.append('')
                        rows.append(row)
                    else:
                        skipped_rows += 1
                        if skipped_rows <= 5:
                            print(f"‚ö†Ô∏è Skipping malformed row {line_num}: wrong column count")

                except Exception as e:
                    skipped_rows += 1
                    if skipped_rows <= 5:
                        print(f"‚ö†Ô∏è Skipping row {line_num}: {str(e)}")

        if not rows:
            raise Exception("‚ùå No valid rows found in CSV")

        df = pd.DataFrame(rows, columns=headers)
        print(f"‚úÖ CSV reconstructed! Shape: {df.shape}, Skipped: {skipped_rows} rows")
        return df

